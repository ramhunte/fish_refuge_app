####################################################################
############################## UI ##############################
####################################################################
summary_ui <- function(id) {
  ns <- NS(id)
  tagList(
    verbatimTextOutput(ns("summary"))
  )
}


####################################################################
############################## server ##############################
####################################################################
summary_server <- function(id) {
  moduleServer(id, function(input, output, session) {
    output$summary <- renderPrint({
      design_inputs()
    })
  })
}
